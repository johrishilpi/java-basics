package learnjava;

public class Parametrized_Static {

	static void shilpi(boolean a)
	{
		System.out.println("True or false");
	}
	static void sj(char b)
	{
	System.out.println("Character Pillars");
	}
	
	static void vj(boolean c,char d)
	{
		System.out.println("Truth and honesty are great character pillars");
	}
	public static void main(String[] args) {
	sj('L');
		shilpi(true);
		shilpi(false);
		sj('h');
vj(true,'y');
	}

}
