package go_with_the_flow;

public class Day6_Update_value_of_local_var {
public static void main(String[] args) {
	int age;//declaration
	age=20;//initialization
	System.out.println(age);
	age=21;//updating the local variable age
	System.out.println(age);
	
	String name="Shilpi";//declaration and initialization
	System.out.println(name);
	name="Johri";//updating the value of local variable name
	System.out.println(name);
	
	String n;//declaration
	n="Hello";//initialization
	System.out.println(n);
	n="Hello world";//updating the value of local variable n
	System.out.println(n);
	
	
short a=2;
a=7;//updating the value of local variable a
	System.out.println(a);
	
	}

}
