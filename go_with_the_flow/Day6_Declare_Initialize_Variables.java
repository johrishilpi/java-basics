System.out.println(salary);

float weight=9.987f;//Declaration and initialization of float datatype
System.out.println(weight);

double pi_value=3.14159265358979323;//Declaration and initialization of double datatype
System.out.println(pi_value);

char starting_alpha='I';
System.out.println(starting_alpha);

